# int Datatype
roll_no = 35
id = int(13)
print('Your roll_no is:', roll_no)
print('Your id is:', id)
print(type(roll_no))
print(type(id))


# float Datatype
height = 155.50
weight = float(63.50)
print('Your height is:', height)
print('Your Weight is:', weight)
print(type(height))
print(type(weight))

# complex Datatype
a = 5+6j
print('The Complex Number is:', a)
